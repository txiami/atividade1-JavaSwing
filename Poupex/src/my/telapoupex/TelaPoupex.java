    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int anos;

        double juros;

        double depositoMensal;

        double total = 0;

        anos = Integer.parseInt(inputNumAnos.getText());

        juros = Double.parseDouble(inputJurosMes.getText()) / 100;

        depositoMensal = Double.parseDouble(inputDepositoMensal.getText());

        int num_pagamentos = anos * 12;

        for (int i = 0; i < num_pagamentos; i++) {

            total = total + depositoMensal;

            total = total + (total * juros);

        }

        outputRESULTADO.setText(String.format("%.2f", total));


    }//GEN-LAST:event_jButton1ActionPerformed

